# ---------------- Image Preprocessing ----------------
def preprocess_image(image):
    image = image.resize((224, 224))
    image = np.array(image).astype("float32")
    image = tf.keras.applications.efficientnet.preprocess_input(image)
    image = np.expand_dims(image, axis=0)
    return image

# ---------------- Routes ----------------
@app.route("/")
def home():
    return "Tamil Letter Recognition API Running"

@app.route("/predict", methods=["POST"])
def predict():
    if "image" not in request.files:
        return jsonify({"error": "No image uploaded"}), 400

    file = request.files["image"]
    image = Image.open(file).convert("RGB")
    image = preprocess_image(image)

    predictions = model.predict(image)[0]
    predicted_index = int(np.argmax(predictions))

    # Get number from class_names.json
    predicted_number = class_names[predicted_index]

    # Map number to Tamil letter
    prediction_letter = tamil_map.get(predicted_number, "Unknown")

    confidence = float(predictions[predicted_index])

    return jsonify({
        "prediction": prediction_letter,
        "confidence": confidence
    })

